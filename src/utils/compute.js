export const filterByMonth = (transactions, year, month) =>
  transactions.filter(t => {
    const d = new Date(t.date + "T00:00:00");
    return d.getFullYear() === year && d.getMonth() + 1 === month;
  });

export const getMonths = (transactions) => {
  const set = new Set(transactions.map(t => t.date.slice(0, 7)));
  return Array.from(set).sort().reverse();
};

// Only positive (debit) amounts are spending. Negative amounts are credits —
// refunds, reimbursements, and credit-card payments — and must not cancel out
// real spend, otherwise monthly totals collapse toward zero. Transfers
// (credit-card settlements, EFT moves between accounts) are money movement,
// not consumption, so they are excluded from spend totals too.
export const totalExpenses = (transactions) =>
  transactions.filter(t => t.category !== "Income" && t.category !== "Transfer")
    .reduce((a, t) => a + (t.amount > 0 ? t.amount : 0), 0);

export const sumByCategory = (transactions) =>
  transactions.reduce((acc, t) => {
    if (!acc[t.category]) acc[t.category] = 0;
    acc[t.category] += t.amount;
    return acc;
  }, {});

export const sumByVendor = (transactions) =>
  transactions.reduce((acc, t) => {
    if (!acc[t.vendor]) acc[t.vendor] = 0;
    acc[t.vendor] += t.amount;
    return acc;
  }, {});

export const calcNetWorth = (assets) => {
  const totalAssets = assets.filter(a => a.type === "asset").reduce((s, a) => s + a.value, 0);
  const totalLiabilities = assets.filter(a => a.type === "liability").reduce((s, a) => s + a.value, 0);
  return { totalAssets, totalLiabilities, netWorth: totalAssets - totalLiabilities };
};

export const fmt = (n, currency = "ZAR") => {
  const prefix = currency === "USD" ? "$" : "R";
  return `${prefix} ${Math.abs(n).toLocaleString("en-ZA", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
};

export const MONTH_LABELS = ["Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"];

export const monthLabel = (ym) => {
  const [y, m] = ym.split("-");
  return `${MONTH_LABELS[parseInt(m, 10) - 1]} ${y}`;
};
